#include <iostream>
using namespace std;

int main()
{

    int day=10;

    switch(day)
    {
        case 1:
            cout<<"Monday"<<endl;
            break;
        
        case 2:
            cout<<"Tuesday"<<endl;
            break;
        
        case 3:
            cout<<"Wednesday"<<endl;
            break;
        
        default:
            cout<<"Error"<<endl;
            break;
    }
    
    return 0;
}


